package fundamentos;

public class Inferencia {

    public static void main(String[] args) {

        var a = 4.5;
        System.out.println(a);

        a = 12.57;
        System.out.println(a);

        var c = "Texto";
        System.out.println(c);

        c = "Outro texto";
        System.out.println(c);

    }

}
