package streams.desafios;

public class Pesquisa {

    final Integer nota;
    final String nomeUsuario;
    final Boolean recomendariaParaAmigos;

    public Pesquisa(Integer nota, String nomeUsuario, Boolean recomendariaParaAmigos) {
        this.nota = nota;
        this.nomeUsuario = nomeUsuario;
        this.recomendariaParaAmigos = recomendariaParaAmigos;
    }
}
